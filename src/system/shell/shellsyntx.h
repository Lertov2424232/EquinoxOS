#ifndef SHELL_SYNTX_H
#define SHELL_SYNTX_H

#include <stdbool.h>
#include <stdint.h>

#include "shell.h" // подтягивает shell_output_fn и emergency-сигналы

// =============================================================================
//          shellsyntx — описание языка ring-0 оболочки EquinoxOS
// =============================================================================
//
// Этот заголовок выносит из shell.c "грамматику" оболочки:
//   1. Тип данных команды (verb + handler + help).
//   2. Таблицу `SHELL_COMMANDS[]` — единственный источник правды о том,
//      какие команды есть, какие из них принимают аргументы, и что
//      выводит `help`.
//   3. Программный entry point `shell_execute_line()` — позволяет
//      запустить команду ОТКУДА УГОДНО (init.lua, syscall, emergency-
//      рендер, тест) с указанием, куда писать вывод.
//
// Раньше ring-3 Lua-терминал в sysgui (init.lua) дублировал у себя
// собственный парсер команд и набор реализаций (ps, kill, killall).
// Теперь GUI-терминал отдаёт строку прямо в ring-0 shell через
// `SYS_SHELL_EXEC` (см. syscall.c, case 73) и просто отображает её
// вывод — никакого второго реестра команд нет.

// ------------- Тип одной команды ---------------------------------------

/* Обработчик команды. `args` — указатель на хвост строки сразу после
 * самого верба (и одиночного пробела); для verb-only команд = "".
 * Вывод пишется через текущий sink (shell_get_output / shell_set_output;
 * sh_print внутри shell.c). Возвращаемого значения нет — статус идёт
 * текстом в вывод. */
typedef void (*shell_cmd_fn)(const char *args);

typedef struct {
    const char  *verb;       // ключевое слово ("ls", "kill", "run", ...)
    bool         takes_args; // true => verb должен заканчиваться пробелом
                             //         перед аргументами ("kill 5"),
                             //         иначе он матчится точным strcmp
                             //         ("ls", "ps", "help").
    shell_cmd_fn handler;    // реализация
    const char  *help;       // одна строка для `help`
} shell_command_t;

// ------------- Реестр команд (см. shell.c) -----------------------------

extern const shell_command_t SHELL_COMMANDS[];
extern const int             SHELL_COMMANDS_COUNT;

// ------------- Публичный API ------------------------------------------

/* Найти команду по началу строки. Возвращает NULL, если ничего не
 * подходит. Сравнение учитывает takes_args: для verb-only команд
 * проверяется полное равенство, для команд с аргументами — что строка
 * начинается с "<verb> ". */
const shell_command_t *shell_find_command(const char *line);

/* Однократно выполнить готовую строку.
 *   line — команда без перевода строки в конце.
 *   out  — sink для вывода (NULL => не менять текущий, использовать как есть).
 *
 * В отличие от внутреннего диспетчера, который вешает шелл на свой
 * GUI-терминал (печатает приглашение, пишет в историю), эта функция:
 *   • НЕ дёргает prompt и историю;
 *   • временно подменяет sink на `out`, гарантированно возвращает
 *     прежний по выходе;
 *   • пригодна для вызова из syscall-обработчика (SYS_SHELL_EXEC) и
 *     из ring-3 приложений через эту syscall'у. */
void shell_execute_line(const char *line, shell_output_fn out);

#endif /* SHELL_SYNTX_H */
